<?php

// LA BOUCLE DO - WHILE
// le code ci présent sera traité au moins une seule fois
// et a la fin seon si ca retombe vrai ou faux, il re traitera le code.    
$x = 0;

do {
    
    echo 'Le nombre est égal : '.$x.'<br/>';
    // Ici la boucle va rajouter +1 a $x a chaque tour de la boucle. 
    $x++;
} while ($x < 10)




?>

